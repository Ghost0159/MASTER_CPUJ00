/*---------------------------------------------------------------------------*
 *	typedef定義
 *---------------------------------------------------------------------------*/
// キー入力情報
typedef struct KeyInfo
{
	u16     cnt;    // 未加工入力値
	u16     trg;    // 押しトリガ入力
} KeyInfo;

/*---------------------------------------------------------------------------*
 *	prototype宣言
 *---------------------------------------------------------------------------*/
void KeyRead   ( void );

/*---------------------------------------------------------------------------*
 *	inline関数
 *---------------------------------------------------------------------------*/
extern KeyInfo	keyData;

inline BOOL KEY_TRIGGER( u16 trg )
{
	return ( keyData.trg & trg );
}

inline BOOL KEY_CONT( u16 cnt )
{
	return ( keyData.cnt & cnt );
}


